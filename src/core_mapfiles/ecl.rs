pub(super) fn core_signatures(game: Game) -> &'static CoreSignatures {
    match game {
        Th06 => ECL_06,
        Th07 => ECL_07,
        Th08 | Th09 => ECL_08_09,

        Th095 | Th10 | Alcostg | Th11 | Th12 | Th125 | Th128 |
        Th13 | Th14 | Th143 | Th15 | Th16 | Th165 | Th17 | Th18 => CoreSignatures::EMPTY,
    }
}

